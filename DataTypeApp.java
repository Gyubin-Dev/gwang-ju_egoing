
public class DataTypeApp {

	public static void main(String[] args) {
		
		System.out.println(12345123); //Integer 
		System.out.println("Hello World!!"+"\n"); //String
		System.out.println('1');
		System.out.println(1+1); //산술 연산자 2
		System.out.println("1"+"1"+"\n"); //결합연산자
		
		System.out.println(1.8); //double
		System.out.println(Math.floor(1.8));
		System.out.println(Math.ceil(2.3)+"\n");
		
		System.out.println("hello world".length());
		System.out.println("hello world".contains("world"));
		
		
		
		
		
	}

}
